-- Esquema inicial para Supabase/PostgreSQL
-- Sistema de Gestion de Boletos para Jubilados - Obera
--
-- Pensado para ejecutarse en el SQL Editor de Supabase.
-- Usa auth.users para registro/login y public.profiles para datos internos.

begin;

create extension if not exists pgcrypto;

-- =========================
-- Catalogos y perfiles
-- =========================

create table if not exists public.roles (
  id smallint generated by default as identity primary key,
  code text not null unique,
  name text not null,
  description text,
  created_at timestamptz not null default now(),
  constraint roles_code_check check (code in ('admin', 'supervisor'))
);

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  role_id smallint not null references public.roles(id),
  username text not null unique,
  full_name text not null,
  email text unique,
  active boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.beneficiary_statuses (
  code text primary key,
  name text not null,
  description text
);

create table if not exists public.card_statuses (
  code text primary key,
  name text not null,
  description text
);

create table if not exists public.anomaly_types (
  code text primary key,
  name text not null,
  description text
);

create table if not exists public.anomaly_statuses (
  code text primary key,
  name text not null
);

create table if not exists public.incident_priorities (
  code text primary key,
  name text not null,
  sort_order smallint not null unique
);

create table if not exists public.incident_statuses (
  code text primary key,
  name text not null
);

-- =========================
-- Configuracion
-- =========================

create table if not exists public.recharge_policies (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  max_tickets_per_operation integer not null,
  monthly_ticket_quota integer,
  valid_from date not null default current_date,
  valid_until date,
  active boolean not null default true,
  created_by uuid references public.profiles(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint recharge_policies_max_positive check (max_tickets_per_operation > 0),
  constraint recharge_policies_quota_positive check (monthly_ticket_quota is null or monthly_ticket_quota > 0),
  constraint recharge_policies_valid_range check (valid_until is null or valid_until >= valid_from)
);

create unique index if not exists recharge_policies_one_active_idx
  on public.recharge_policies(active)
  where active;

create table if not exists public.transport_lines (
  id uuid primary key default gen_random_uuid(),
  code text not null unique,
  name text not null,
  description text,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- =========================
-- Padron, tarjetas y recargas
-- =========================

create table if not exists public.beneficiaries (
  id uuid primary key default gen_random_uuid(),
  legacy_code text unique,
  first_name text not null,
  last_name text not null,
  dni text not null unique,
  birth_date date not null,
  street_address text not null,
  neighborhood text not null,
  city text not null default 'Obera',
  status_code text not null references public.beneficiary_statuses(code) default 'activo',
  available_tickets integer not null default 0,
  enrolled_at date not null default current_date,
  created_by uuid references public.profiles(id),
  updated_by uuid references public.profiles(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint beneficiaries_dni_digits check (dni ~ '^[0-9]{7,8}$'),
  constraint beneficiaries_available_tickets_nonnegative check (available_tickets >= 0)
);

create index if not exists beneficiaries_name_idx
  on public.beneficiaries(last_name, first_name);

create index if not exists beneficiaries_status_idx
  on public.beneficiaries(status_code);

create table if not exists public.cards (
  id uuid primary key default gen_random_uuid(),
  beneficiary_id uuid not null references public.beneficiaries(id) on delete restrict,
  card_number text unique,
  status_code text not null references public.card_statuses(code) default 'pendiente',
  issued_at date,
  block_reason text,
  replaced_by_card_id uuid references public.cards(id),
  created_by uuid references public.profiles(id),
  updated_by uuid references public.profiles(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint cards_number_required_when_issued check (
    (status_code = 'pendiente' and card_number is null)
    or (status_code <> 'pendiente' and card_number is not null)
  )
);

create unique index if not exists cards_one_active_per_beneficiary_idx
  on public.cards(beneficiary_id)
  where status_code = 'activa';

create index if not exists cards_beneficiary_idx
  on public.cards(beneficiary_id);

create table if not exists public.card_events (
  id uuid primary key default gen_random_uuid(),
  card_id uuid not null references public.cards(id) on delete cascade,
  event_type text not null,
  reason text,
  performed_by uuid references public.profiles(id),
  created_at timestamptz not null default now(),
  constraint card_events_type_check check (event_type in ('emitida', 'bloqueada', 'desbloqueada', 'reemplazo_solicitado'))
);

create table if not exists public.recharges (
  id uuid primary key default gen_random_uuid(),
  beneficiary_id uuid not null references public.beneficiaries(id) on delete restrict,
  policy_id uuid not null references public.recharge_policies(id),
  quantity integer not null,
  operator_id uuid not null references public.profiles(id),
  performed_at timestamptz not null default now(),
  notes text,
  created_at timestamptz not null default now(),
  constraint recharges_quantity_positive check (quantity > 0)
);

create index if not exists recharges_beneficiary_performed_idx
  on public.recharges(beneficiary_id, performed_at desc);

create index if not exists recharges_operator_idx
  on public.recharges(operator_id);

-- =========================
-- Operacion: viajes, anomalias e incidentes
-- =========================

create table if not exists public.trips (
  id uuid primary key default gen_random_uuid(),
  line_id uuid not null references public.transport_lines(id) on delete restrict,
  service_date date not null,
  service_time time not null,
  tickets_used integer not null default 0,
  successful_validations integer not null default 0,
  rejected_validations integer not null default 0,
  reported_by uuid references public.profiles(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
