create index if not exists trips_date_line_idx
  on public.trips(service_date desc, line_id);

create table if not exists public.anomalies (
  id uuid primary key default gen_random_uuid(),
  beneficiary_id uuid references public.beneficiaries(id) on delete set null,
  card_id uuid references public.cards(id) on delete set null,
  type_code text not null references public.anomaly_types(code),
  status_code text not null references public.anomaly_statuses(code) default 'pendiente',
  detail text not null,
  detected_at timestamptz not null default now(),
