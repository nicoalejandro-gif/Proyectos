create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  default_role_id smallint;
  desired_username text;
begin
  select id into default_role_id from public.roles where code = 'supervisor';

  desired_username := coalesce(
    nullif(new.raw_user_meta_data ->> 'username', ''),
    split_part(new.email, '@', 1),
    'usuario_' || left(new.id::text, 8)
  );

  insert into public.profiles (id, role_id, username, full_name, email, active)
  values (
    new.id,
    default_role_id,
    desired_username,
    coalesce(nullif(new.raw_user_meta_data ->> 'full_name', ''), desired_username),
    new.email,
    false
  );

  return new;
end;
$$;

create or replace function public.protect_profile_role_and_active()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.is_admin() then
    if new.role_id is distinct from old.role_id or new.active is distinct from old.active then
      raise exception 'Solo un administrador puede modificar rol o estado de activacion.';
    end if;
  end if;

  return new;
end;
$$;

create or replace function public.validate_and_apply_recharge()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  policy_record public.recharge_policies%rowtype;
  month_total integer;
begin
  select *
  into policy_record
  from public.recharge_policies
  where id = new.policy_id
    and active = true
    and valid_from <= new.performed_at::date
    and (valid_until is null or valid_until >= new.performed_at::date);

  if not found then
    raise exception 'La politica de recarga no esta activa o vigente.';
  end if;

  if new.quantity > policy_record.max_tickets_per_operation then
    raise exception 'La recarga supera el maximo de boletos por operacion.';
  end if;

  if policy_record.monthly_ticket_quota is not null then
    select coalesce(sum(quantity), 0)
    into month_total
    from public.recharges
    where beneficiary_id = new.beneficiary_id
      and performed_at >= date_trunc('month', new.performed_at)
      and performed_at < date_trunc('month', new.performed_at) + interval '1 month';

    if month_total + new.quantity > policy_record.monthly_ticket_quota then
      raise exception 'La recarga supera el cupo mensual configurado.';
    end if;
  end if;

  update public.beneficiaries
  set available_tickets = available_tickets + new.quantity,
      updated_at = now(),
      updated_by = new.operator_id
  where id = new.beneficiary_id;

  return new;
end;
$$;

create or replace function public.normalize_anomaly_resolution()
returns trigger
language plpgsql
as $$
begin
  if new.status_code in ('resuelta', 'descartada') and new.resolved_at is null then
    new.resolved_at = now();
  elsif new.status_code not in ('resuelta', 'descartada') then
    new.resolved_at = null;
  end if;

  return new;
end;
$$;

create or replace function public.normalize_incident_closed_at()
returns trigger
language plpgsql
as $$
begin
  if new.status_code = 'cerrado' and new.closed_at is null then
    new.closed_at = now();
  elsif new.status_code <> 'cerrado' then
    new.closed_at = null;
  end if;

  return new;
end;
$$;

create or replace function public.log_audit(
  p_action text,
  p_entity_table text,
  p_entity_id uuid,
  p_detail text,
  p_metadata jsonb default '{}'::jsonb
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_actor_name text;
begin
  select full_name into v_actor_name
  from public.profiles
  where id = auth.uid();

  insert into public.audit_logs (
    actor_id,
    actor_name,
    action,
    entity_table,
    entity_id,
    detail,
    metadata
  )
  values (
    auth.uid(),
    coalesce(v_actor_name, 'Sistema'),
    p_action,
    p_entity_table,
    p_entity_id,
    p_detail,
    coalesce(p_metadata, '{}'::jsonb)
  );
end;
$$;

create or replace function public.audit_row_changes()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_action text;
  v_id uuid;
begin
  v_action := tg_table_name || '_' || lower(tg_op);

  if tg_op = 'DELETE' then
    v_id := old.id;
  else
    v_id := new.id;
  end if;

  perform public.log_audit(
    v_action,
    tg_table_name,
    v_id,
    case tg_op
      when 'INSERT' then 'Registro creado'
      when 'UPDATE' then 'Registro actualizado'
      when 'DELETE' then 'Registro eliminado'
    end,
    jsonb_build_object('operation', tg_op)
  );

  if tg_op = 'DELETE' then
    return old;
  end if;

  return new;
end;
$$;

