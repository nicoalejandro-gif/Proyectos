create index if not exists anomalies_status_detected_idx
  on public.anomalies(status_code, detected_at desc);

create index if not exists anomalies_beneficiary_idx
  on public.anomalies(beneficiary_id);

create table if not exists public.anomaly_observations (
  id uuid primary key default gen_random_uuid(),
  anomaly_id uuid not null references public.anomalies(id) on delete cascade,
